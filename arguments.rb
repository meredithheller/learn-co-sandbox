def say_whats_up(your_name,my_name)
  puts "What's up #{your_name}, I am #{my_name}."
end

say_whats_up("Ella","Meredith")

def say_whats_upp(your_name,my_name = "Meredith")
  puts "What's up #{your_name}, I am #{my_name}."
end

say_whats_upp("Ella", "Umahani")
say_whats_upp("Umahani")
say_whats_upp("Dami")

